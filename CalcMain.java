    // Calculate:
    public void write(String chr) {
        //write to current value
        if (jTextField1.isFocusOwner() == true) {
            num1 = num1 + chr;
            jTextField1.setText(num1);
        } else {
            num2 = num2 + chr;
            jTextField2.setText(num2);
        }
    }

    //Select Operation
    public void writeOp(String O) {
        jTextField3.setText(O);
        Op = O;
        jTextField3.requestFocus();
    }

    // Put to total:
    public void writeTotal() {
        //Write current value to running total        
        newTally = newTally + num1 + " " + Op + " " + num2 + " " + "\n\r";//Write current Number
        //Convert and Calculate total
        firstNum = Double.parseDouble(num1);
        secondNum = Double.parseDouble(num2);
        if (Op == "+") {
            total = firstNum + secondNum;
        } else if (Op == "-") {
            total = firstNum - secondNum;
        } else if (Op == "*") {
            total = firstNum * secondNum;
        } else if (Op == "/") {
            total = firstNum / secondNum;
        } else {
            JOptionPane.showMessageDialog(null, "InfoBox: ");
        }
        //write numeric total
        newTally = newTally + "  " + total + "\n\r" + "\n\r";

        //Clear text and values
        clear();
    }

    public void clear() {
        num1 = "";
        num2 = "";
        Op = "";
        jTextArea1.setText(newTally);
        jTextField1.setText(num1);
        jTextField2.setText(num2);
        jTextField3.setText(Op);
        jTextField1.requestFocus();
    }

